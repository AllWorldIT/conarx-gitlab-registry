func testingKey(s string) string { return strings.ReplaceAll(s, "TESTING KEY", "PRIVATE KEY") }

func generateTempFile(tb testing.TB, filename string, content []byte) *os.File {
	dir := tb.TempDir()
	name := filepath.Join(dir, filename)

	f, err := os.Create(name)
	require.NoError(tb, err)
	defer f.Close()

	_, err = f.Write(content)
	require.NoError(tb, err)

	return f
}

func TLSCertFilename(tb testing.TB) string {
	f := generateTempFile(tb, "cert.pem", []byte(ecdsaCertPEM))

	return f.Name()
}

func TLSKeytFilename(tb testing.TB) string {
	f := generateTempFile(tb, "key.pem", []byte(ecdsaKeyPEM))

	return f.Name()
}
